/**
 * Concatenates all given [SemiOrderedLikeArgsGenerator] resulting in an [SemiOrderedArgsGenerator] which yields
 * the values of the first [SemiOrderedLikeArgsGenerator], then the second generator ... up to the values of
 * the last generator and then starting over.
 *
 * I.e. the resulting [SemiOrderedArgsGenerator] generates the sum of [SemiOrderedLikeArgsGenerator.size] values
 * before repeating.
 *
 * @param T The type of values generated by the [SemiOrderedLikeArgsGenerator]s.
 *
 * @return The resulting [SemiOrderedArgsGenerator] after concatenation.
 *
 * @since 3.0.0
 */
fun <T> Sequence<SemiOrderedLikeArgsGenerator<T>>.concatAll(): SemiOrderedLikeArgsGenerator<T> = concatAll(iterator())

